def sum(x, y):
    soma = x + y
    print(f'{x} + {y} = {soma}')
    


def sub(x, y):
    subtracao = x - y
    print(f'{x} - {y} = {subtracao}')
    


def multi(x, y):
    multiplicacao = x * y
    print(f'{x} * {y} = {multiplicacao}')
    


def division(x, y):
    divisao = x / y
    print(f'{x} / {y} = {divisao}')
    








